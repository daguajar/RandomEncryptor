package cl.daguajar.re.auxClasses;

public class AmountPerChar {

	private int id;
	private long amount;
	
	public AmountPerChar(int id, long amount) {
		super();
		this.id = id;
		this.amount = amount;
	}

	public int getId() {
		return id;
	}

	public long getAmount() {
		return amount;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setAmount(long amount) {
		this.amount = amount;
	}

	
	
}
